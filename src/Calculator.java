
public class Calculator {
	
	int add(int numOne, int numTwo) {
		
		return numOne + numTwo;
	}
	int subtract(int numOne, int numTwo) {
		
		return numOne - numTwo;
	}
	int multiply(int numOne, int numTwo) {
	
		return numOne * numTwo;
	}
	int divide(int numOne, int numTwo) {
	
		return numOne / numTwo;
	}
	
	
	
}
